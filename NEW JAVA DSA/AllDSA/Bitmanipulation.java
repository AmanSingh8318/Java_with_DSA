public class Bitmanipulation {
    public static void main(String[] args) {
        // And operation
        System.out.println(5&6);

        // OR operation
        System.out.println(5|6);
        // xor operation
        System.out.println(5^6);
        //Left Shift
        //derived fromula for left shift a<<b=a*b^2
        System.out.println(5<<2);
        // Right Shift
        //derived formula for right shift a>>b a>>b=a/2^b
        System.out.println(5>>1);

    }
}
